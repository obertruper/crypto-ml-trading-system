#!/bin/bash
# Скрипт для тестирования SSH подключения к Vast.ai

echo "Тестирование SSH подключения к Vast.ai серверу..."
echo "================================================"

# Цвета для вывода
GREEN='\033[0;32m'
RED='\033[0;31m'
NC='\033[0m' # No Color

# Тест 1: Прямое подключение
echo -e "\n1. Тестируем прямое подключение (79.116.73.220:27681)..."
if ssh -o ConnectTimeout=10 -o BatchMode=yes -p 27681 root@79.116.73.220 -i ~/.ssh/id_rsa 'echo "Успешно подключились!"; hostname; uname -a' 2>/dev/null; then
    echo -e "${GREEN}✓ Прямое подключение работает!${NC}"
else
    echo -e "${RED}✗ Прямое подключение не удалось${NC}"
    
    # Тест 2: Подключение через прокси
    echo -e "\n2. Тестируем подключение через прокси (ssh5.vast.ai:17171)..."
    if ssh -o ConnectTimeout=10 -o BatchMode=yes -p 17171 root@ssh5.vast.ai -i ~/.ssh/id_rsa 'echo "Успешно подключились через прокси!"; hostname; uname -a' 2>/dev/null; then
        echo -e "${GREEN}✓ Подключение через прокси работает!${NC}"
    else
        echo -e "${RED}✗ Подключение через прокси не удалось${NC}"
    fi
fi

echo -e "\n================================================"
echo "Если оба способа не работают:"
echo "1. Используйте веб-терминал Vast.ai"
echo "2. Добавьте SSH ключ вручную (инструкция выше)"
echo "3. Проверьте статус инстанса на vast.ai"